
nombre = input("Ingresa tu nombre: ")
numero = int(input(nombre + " Ingresa el numero que deseas pasar a letras: "))

if numero == 1:
    print("el valor ingresado es uno")
elif numero == 2:
    print("el valor ingresado es dos")
elif numero == 3:
    print("el valor ingresado es tres")
elif numero == 4:
    print("el valor ingresado es cuatro")
elif numero == 5:
    print("el valor ingresado es cinco")
else:
    print("El programa solo puede convertir hasta el numero 5.")